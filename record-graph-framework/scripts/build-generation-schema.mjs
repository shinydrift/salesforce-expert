#!/usr/bin/env node
/**
 * Build step: derive the per-template **generation schema** — the tiny contract
 * the LLM/agent generates against — from the authored YAML templates.
 *
 *   templates/<id>.yaml  ->  schema/generation/<id>.schema.json
 *
 * This is the third tier in the locked two-tier-plus model
 * (generation surface ⊂ template ⊂ base schema, see DESIGN.md): instead of seeing
 * the wide describe-derived base schema, the agent picks a `templateId` and fills
 * only that template's typed parameter slots. Each generated schema validates the
 * generation payload:
 *
 *   { "templateId": "<id>", "parameters": { <slot>: <value>, ... } }
 *
 * Slot types/required/default come straight from the template's `parameters`
 * block — the template is the single source of truth, so there is no second list
 * to keep in sync. The build is atomic (every template is validated and its schema
 * built in memory before anything is written) and prunes orphaned output, so a
 * malformed or renamed template can never leave partial / stale schemas on disk.
 * Output is deterministic (sorted keys) so re-runs produce clean diffs and the
 * generated files can be checked in.
 *
 * Usage: node scripts/build-generation-schema.mjs   (run from the project root)
 */
import { readFileSync, writeFileSync, readdirSync, mkdirSync, existsSync, unlinkSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import yaml from 'js-yaml';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const templatesDir = join(root, 'templates');
const outDir = join(root, 'schema', 'generation');

const GENERATED_MARKER =
  'GENERATED by scripts/build-generation-schema.mjs from the YAML template — do not hand-edit. Run `node scripts/build-generation-schema.mjs` to refresh.';

// Template parameter `type` -> JSON Schema property. Mirrors the value space
// TemplateExpander accepts (string / number); the string-format variants let a
// template tighten a slot (e.g. `type: date`) so the generation surface isn't
// looser than the describe-derived base schema. Unknown types are a hard error
// (see assertParam) rather than a silent string fallback.
const TYPE_MAP = {
  string: { type: 'string' },
  number: { type: 'number' },
  integer: { type: 'integer' },
  boolean: { type: 'boolean' },
  date: { type: 'string', format: 'date' },
  datetime: { type: 'string', format: 'date-time' },
  email: { type: 'string', format: 'email' },
  uri: { type: 'string', format: 'uri' },
};

// JSON Schema type a default value must have, given the slot's mapped `type`.
const JS_TYPE_OF = (v) => (v === null ? 'null' : Array.isArray(v) ? 'array' : typeof v);
function defaultTypeOk(mappedType, value) {
  if (mappedType === 'integer') return Number.isInteger(value);
  if (mappedType === 'number') return typeof value === 'number';
  if (mappedType === 'boolean') return typeof value === 'boolean';
  return typeof value === 'string'; // string + all string-format types
}

/** Validate one parameter entry; throws with a template-scoped message. */
function assertParam(param, templateId, seen) {
  if (!param || typeof param !== 'object' || Array.isArray(param)) {
    throw new Error(`${templateId}: parameter entry is not an object (got ${JS_TYPE_OF(param)})`);
  }
  if (typeof param.name !== 'string' || param.name.trim() === '') {
    throw new Error(`${templateId}: a parameter is missing a non-empty string "name"`);
  }
  if (seen.has(param.name)) {
    throw new Error(`${templateId}: duplicate parameter name "${param.name}"`);
  }
  seen.add(param.name);
  if (!TYPE_MAP[param.type]) {
    throw new Error(
      `${templateId}: parameter "${param.name}" has unsupported type "${param.type}" ` +
        `(expected one of: ${Object.keys(TYPE_MAP).join(', ')})`,
    );
  }
  if (Object.prototype.hasOwnProperty.call(param, 'default') &&
      !defaultTypeOk(TYPE_MAP[param.type].type, param.default)) {
    throw new Error(
      `${templateId}: parameter "${param.name}" default ${JSON.stringify(param.default)} ` +
        `does not match type "${param.type}"`,
    );
  }
}

function propertyForParam(param) {
  const prop = { ...TYPE_MAP[param.type] };
  if (param.format) prop.format = param.format; // explicit override / extra constraint
  if (param.description) prop.description = param.description;
  // Carry the template's default through so tooling can pre-fill it — but never on
  // a required slot, where a default is contradictory (the caller must supply it).
  if (param.required !== true && Object.prototype.hasOwnProperty.call(param, 'default')) {
    prop.default = param.default;
  }
  return prop;
}

/** Build the generation JSON Schema for a parsed template. Throws on a malformed
 *  `parameters` block — the caller builds all schemas before writing any, so a bad
 *  template aborts the whole run with nothing written. Exported for unit tests. */
export function generationSchemaFor(tpl, file) {
  const params = Array.isArray(tpl.parameters) ? tpl.parameters : [];
  const seen = new Set();
  for (const p of params) assertParam(p, tpl.id, seen);

  const sorted = [...params].sort((a, b) => a.name.localeCompare(b.name));

  const properties = {};
  for (const p of sorted) properties[p.name] = propertyForParam(p);

  const required = sorted
    .filter((p) => p.required === true)
    .map((p) => p.name)
    .sort((a, b) => a.localeCompare(b));

  const parametersSchema = {
    type: 'object',
    additionalProperties: false,
    properties,
  };
  if (required.length) parametersSchema.required = required;

  // `parameters` itself is only mandatory when at least one slot is required.
  const topRequired = required.length ? ['templateId', 'parameters'] : ['templateId'];

  const description =
    (tpl.description ? tpl.description + ' ' : '') +
    `${GENERATED_MARKER} Source: templates/${file}.`;

  return {
    $schema: 'http://json-schema.org/draft-07/schema#',
    $id: `https://example.com/schemas/generation/${tpl.id}.schema.json`,
    title: `${tpl.label || tpl.id} — generation surface`,
    description,
    type: 'object',
    additionalProperties: false,
    required: topRequired,
    properties: {
      templateId: {
        const: tpl.id,
        description: 'The template this graph is expanded from.',
      },
      parameters: parametersSchema,
    },
  };
}

/** Serialize a schema the way the build writes it (stable, trailing newline). */
export const serialize = (schema) => JSON.stringify(schema, null, 2) + '\n';

function main() {
  const files = readdirSync(templatesDir)
    .filter((f) => f.endsWith('.yaml') || f.endsWith('.yml'))
    .sort((a, b) => a.localeCompare(b));
  if (files.length === 0) {
    console.error('No YAML templates found in', templatesDir);
    process.exit(1);
  }

  // 1. Build everything in memory first — a throw here writes nothing.
  const built = [];
  for (const file of files) {
    const tpl = yaml.load(readFileSync(join(templatesDir, file), 'utf8'));
    if (!tpl || !tpl.id) {
      console.warn(`skip ${file}: no top-level "id"`);
      continue;
    }
    built.push({ id: tpl.id, file, schema: generationSchemaFor(tpl, file) });
  }

  if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true });

  // 2. Prune orphaned schemas (a deleted/renamed template must not leave a stale
  //    artifact checked in).
  const expected = new Set(built.map((b) => `${b.id}.schema.json`));
  for (const existing of readdirSync(outDir).filter((f) => f.endsWith('.schema.json'))) {
    if (!expected.has(existing)) {
      unlinkSync(join(outDir, existing));
      console.log(`pruned orphaned schema/generation/${existing}`);
    }
  }

  // 3. Write.
  for (const { id, file, schema } of built) {
    writeFileSync(join(outDir, `${id}.schema.json`), serialize(schema));
    const slots = Object.keys(schema.properties.parameters.properties);
    const req = schema.properties.parameters.required || [];
    console.log(
      `built ${file} -> schema/generation/${id}.schema.json ` +
        `(${slots.length} slot(s)${req.length ? `, ${req.length} required` : ''})`,
    );
  }
  console.log(`\nDone: ${built.length} generation schema(s) written to ${outDir}`);
}

// Run as a script, but stay importable (the smoke test imports the builders).
if (process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main();
}
